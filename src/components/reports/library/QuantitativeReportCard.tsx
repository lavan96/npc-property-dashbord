import { format } from 'date-fns';
import { BarChart3, Calendar, Download, Eye, User } from 'lucide-react';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import type { GeneratedReport } from './types';

interface QuantitativeReportCardProps {
  report: GeneratedReport;
  generatorLabel: (id?: string | null) => string;
  onView: (reportId: string) => void;
  onDownloadPDF: (report: GeneratedReport) => void;
}

export function QuantitativeReportCard({ report, generatorLabel, onView, onDownloadPDF }: QuantitativeReportCardProps) {
  return (
    <Card className="overflow-hidden hover:shadow-lg transition-shadow">
      <CardHeader>
        <CardTitle className="text-lg flex items-start justify-between">
          <span className="line-clamp-2">{report.title}</span>
          <BarChart3 className="h-5 w-5 text-muted-foreground flex-shrink-0 ml-2" />
        </CardTitle>
        {report.description && <CardDescription className="line-clamp-2">{report.description}</CardDescription>}
        <CardDescription className="text-xs flex items-center gap-1"><Calendar className="h-3 w-3" />Generated on {format(new Date(report.created_at), 'PPp')}</CardDescription>
        <CardDescription className="text-xs flex items-center gap-1"><User className="h-3 w-3" />Generated by {generatorLabel(report.generated_by)}</CardDescription>
      </CardHeader>
      <CardContent className="space-y-4">
        <div className="space-y-2">
          <div className="flex items-center justify-between text-sm"><span className="text-muted-foreground">Listings analyzed:</span><Badge variant="outline">{report.listing_count}</Badge></div>
          {report.kpis && <div className="flex items-center justify-between text-sm"><span className="text-muted-foreground">Avg. Price:</span><span className="font-medium">${report.kpis.avg_price?.toLocaleString() || 'N/A'}</span></div>}
          {report.analytics?.quality && <div className="flex items-center justify-between text-sm"><span className="text-muted-foreground">Data Quality:</span><span className="font-medium">{report.analytics.quality.avg_confidence?.toFixed(1) || 'N/A'}%</span></div>}
        </div>
        <div className="flex gap-2 pt-2">
          <Button variant="outline" size="sm" onClick={() => onView(report.id)} className="flex-1"><Eye className="mr-1 h-3 w-3" />View</Button>
          <Button variant="default" size="sm" onClick={() => onDownloadPDF(report)} className="flex-1"><Download className="mr-1 h-3 w-3" />PDF</Button>
        </div>
      </CardContent>
    </Card>
  );
}
