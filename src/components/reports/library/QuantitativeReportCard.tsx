import { format } from 'date-fns';
import { BarChart3, Calendar, Download, Eye, Gauge, LineChart, User } from 'lucide-react';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardFooter, CardHeader } from '@/components/ui/card';
import type { GeneratedReport } from './types';

interface QuantitativeReportCardProps {
  report: GeneratedReport;
  generatorLabel: (id?: string | null) => string;
  onView: (reportId: string) => void;
  onDownloadPDF: (report: GeneratedReport) => void;
}

export function QuantitativeReportCard({ report, generatorLabel, onView, onDownloadPDF }: QuantitativeReportCardProps) {
  const averagePrice = report.kpis?.avg_price ? `$${report.kpis.avg_price.toLocaleString()}` : 'N/A';
  const dataQuality = report.analytics?.quality?.avg_confidence != null
    ? `${report.analytics.quality.avg_confidence.toFixed(1)}%`
    : 'N/A';

  return (
    <Card className="group relative overflow-hidden rounded-2xl border border-border/70 bg-card/90 shadow-sm transition-all duration-300 hover:-translate-y-0.5 hover:border-primary/30 hover:shadow-xl hover:shadow-primary/10 dark:bg-background/70">
      <div className="pointer-events-none absolute -right-14 -top-16 h-44 w-44 rounded-full bg-info/10 blur-3xl opacity-0 transition-opacity group-hover:opacity-100" />

      <CardHeader className="relative space-y-4 p-4 pb-3">
        <div className="flex items-start justify-between gap-3">
          <Badge variant="secondary" className="gap-1 bg-info/15 text-xs text-info hover:bg-info/20 dark:bg-info/30 dark:text-info">
            <BarChart3 className="h-3 w-3" />
            Quantitative
          </Badge>
          <div className="flex items-center gap-1.5 text-xs text-muted-foreground">
            <Calendar className="h-3.5 w-3.5" />
            {format(new Date(report.created_at), 'PPp')}
          </div>
        </div>

        <div className="space-y-2">
          <h3 className="line-clamp-2 text-lg font-semibold leading-snug tracking-tight text-foreground">
            {report.title}
          </h3>
          <div className="flex items-center gap-1.5 text-xs text-muted-foreground">
            <User className="h-3.5 w-3.5" />
            Generated by {generatorLabel(report.generated_by)}
          </div>
          {report.description && (
            <p className="line-clamp-2 text-sm leading-6 text-muted-foreground">{report.description}</p>
          )}
        </div>
      </CardHeader>

      <CardContent className="relative space-y-4 px-4 pb-4">
        <div className="grid grid-cols-3 gap-2">
          <MetricTile label="Listings" value={report.listing_count} icon={LineChart} />
          <MetricTile label="Avg. Price" value={averagePrice} icon={BarChart3} />
          <MetricTile label="Quality" value={dataQuality} icon={Gauge} />
        </div>
      </CardContent>

      <CardFooter className="relative flex gap-2 border-t border-border/60 bg-muted/20 p-4">
        <Button variant="default" size="sm" onClick={() => onView(report.id)} className="flex-1 gap-1.5 rounded-xl">
          <Eye className="h-3.5 w-3.5" />
          View
        </Button>
        <Button variant="outline" size="sm" onClick={() => onDownloadPDF(report)} className="flex-1 gap-1.5 rounded-xl">
          <Download className="h-3.5 w-3.5" />
          PDF
        </Button>
      </CardFooter>
    </Card>
  );
}

function MetricTile({ label, value, icon: Icon }: { label: string; value: string | number; icon: typeof BarChart3 }) {
  return (
    <div className="rounded-2xl border border-border/60 bg-background/65 p-3 shadow-inner shadow-sm dark:shadow-black/5">
      <div className="mb-2 flex items-center justify-between gap-2 text-muted-foreground">
        <span className="text-[10px] font-semibold uppercase tracking-[0.14em]">{label}</span>
        <Icon className="h-3.5 w-3.5 text-info/80 dark:text-info/80" />
      </div>
      <div className="truncate text-sm font-semibold text-foreground">{value}</div>
    </div>
  );
}
