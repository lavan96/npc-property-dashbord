import { format } from 'date-fns';
import {
  Building2,
  Calendar,
  Download,
  Eye,
  Globe,
  Home,
  Layers3,
  Map,
  MapPin,
  Sparkles,
  TrendingUp,
  User,
  Zap,
} from 'lucide-react';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardFooter, CardHeader } from '@/components/ui/card';
import { Checkbox } from '@/components/ui/checkbox';
import { RegenerateReportButton } from '@/components/reports/RegenerateReportButton';
import { type ReportTier } from '@/components/reports/TierBadge';
import { getInvestmentGradeTone, getInvestmentScoreSummary, getScoreTone } from '@/components/reports/report-view/utils';
import type { InvestmentReport } from './types';
import { resolveInvestmentReportType } from '@/lib/reports/reportVariants';
import { ReportTypeBadge } from '@/components/reports/ReportTypeBadge';

interface InvestmentReportCardProps {
  report: InvestmentReport;
  isSelected: boolean;
  canAddMore: boolean;
  isAutoGenerated: boolean;
  generatingTier: ReportTier | null;
  canEditReports: boolean;
  generatorLabel: (id?: string | null) => string;
  getGradeColor: (grade: string) => string;
  getScoreColor: (score: number) => string;
  onToggleSelection: (report: InvestmentReport, checked: boolean) => void;
  onView: (report: InvestmentReport) => void;
  onDownload: (report: InvestmentReport) => void;
  onRegenerated: () => void;
  onViewHistory: (report: InvestmentReport) => void;
  onToggleArchive: (report: InvestmentReport) => void;
  onGenerateTier: (report: InvestmentReport, targetTier: ReportTier) => void;
}

const scopeMeta = {
  address: { label: 'Property Analysis', icon: Home, className: 'bg-info/15 text-info hover:bg-info/20 dark:bg-info/30 dark:text-info' },
  suburb: { label: 'Suburb Analysis', icon: Building2, className: 'bg-success/15 text-success hover:bg-success/20 dark:bg-success/30 dark:text-success' },
  zipcode: { label: 'Postcode Analysis', icon: Map, className: 'bg-brand-100 text-brand-700 hover:bg-brand-200 dark:bg-brand-900/30 dark:text-brand-300' },
  state: { label: 'State Analysis', icon: Globe, className: 'bg-accent/15 text-accent hover:bg-accent/20 dark:bg-accent/30 dark:text-accent' },
} as const;

export function InvestmentReportCard({
  report,
  isSelected,
  canAddMore,
  isAutoGenerated,
  generatingTier,
  canEditReports,
  generatorLabel,
  getGradeColor,
  getScoreColor,
  onToggleSelection,
  onView,
  onDownload,
  onRegenerated,
  onViewHistory,
  onToggleArchive,
  onGenerateTier,
}: InvestmentReportCardProps) {
  const scope = report.report_scope && report.report_scope in scopeMeta
    ? scopeMeta[report.report_scope as keyof typeof scopeMeta]
    : null;
  const ScopeIcon = scope?.icon;
  const scoreSummary = getInvestmentScoreSummary(report as any);
  const hasGradeDisplay = !!(scoreSummary?.grade || scoreSummary?.recommendation || scoreSummary?.score != null || scoreSummary?.partialLabel);
  const scoreType = report.investment_score?.scoreType === 'area' ? 'Area Grade' : 'Investment Grade';
  const recommendation = scoreSummary?.recommendation || 'Score calculated from market, financial & location data';
  const hasAreaPlaceholder = !hasGradeDisplay && !report.investment_score && ['suburb', 'zipcode', 'state'].includes(report.report_scope || '');
  const reportType = resolveInvestmentReportType(report);

  return (
    <Card
      className={`group relative overflow-hidden rounded-2xl border bg-card/90 shadow-sm transition-all duration-300 hover:-translate-y-0.5 hover:border-primary/30 hover:shadow-xl hover:shadow-primary/10 dark:bg-background/70 ${
        isSelected ? 'border-brand-400/60 ring-2 ring-brand-400/25 shadow-lg shadow-brand-500/10' : 'border-border/70'
      }`}
    >
      <div className="pointer-events-none absolute -right-14 -top-16 h-44 w-44 rounded-full bg-brand-400/10 blur-3xl opacity-0 transition-opacity group-hover:opacity-100" />

      <CardHeader className="relative space-y-4 p-4 pb-3">
        <div className="flex items-start justify-between gap-3">
          <div className="flex min-w-0 flex-1 items-center gap-2">
            <Checkbox
              checked={isSelected}
              onCheckedChange={(checked) => onToggleSelection(report, checked as boolean)}
              disabled={!canAddMore && !isSelected}
              className="mt-0.5 h-5 w-5 shrink-0 border-2 bg-background shadow-sm"
              aria-label={`Select ${report.property_address} for comparison`}
            />
            <div className="flex min-w-0 flex-wrap items-center gap-2">
              {scope && ScopeIcon && (
                <Badge variant="secondary" className={`w-fit gap-1 text-xs ${scope.className}`}>
                  <ScopeIcon className="h-3 w-3" />
                  {scope.label}
                </Badge>
              )}
              <ReportTypeBadge type={reportType} />
            </div>
          </div>

          <div className="flex shrink-0 items-center gap-2">
            {isAutoGenerated && (
              <Badge variant="secondary" className="gap-1 bg-accent/15 text-xs text-accent hover:bg-accent/20 dark:bg-accent/30 dark:text-accent">
                <Zap className="h-3 w-3" />
                Auto
              </Badge>
            )}
            {report.status && report.status !== 'completed' && (
              <Badge variant="outline" className="text-xs capitalize">
                {report.status}
              </Badge>
            )}
          </div>
        </div>

        <div className="space-y-3">
          <div className="flex items-start gap-3">
            <div className="mt-0.5 rounded-xl border border-border/60 bg-background/70 p-2 text-brand-600 dark:text-brand-300">
              <MapPin className="h-4 w-4" />
            </div>
            <div className="min-w-0 flex-1">
              <h3 className="line-clamp-2 text-lg font-semibold leading-snug tracking-tight text-foreground">
                {report.property_address}
              </h3>
              <div className="mt-2 grid gap-1.5 text-xs text-muted-foreground sm:grid-cols-2">
                <span className="flex items-center gap-1.5">
                  <Calendar className="h-3.5 w-3.5" />
                  {format(new Date(report.created_at), 'PPp')}
                </span>
                <span className="flex items-center gap-1.5">
                  <User className="h-3.5 w-3.5" />
                  {generatorLabel(report.generated_by)}
                </span>
                <span className="flex items-center gap-1.5">
                  <Layers3 className="h-3.5 w-3.5" />
                  Version {report.current_version || 1}
                </span>
              </div>
            </div>
          </div>
        </div>
      </CardHeader>

      <CardContent className="relative space-y-4 px-4 pb-4">
        {hasGradeDisplay && scoreSummary ? (
          <div className="rounded-2xl border border-border/70 bg-gradient-to-br from-background via-muted/25 to-background p-3 shadow-inner shadow-sm dark:shadow-black/5">
            <div className="flex items-center justify-between gap-4">
              <div className="flex min-w-0 items-center gap-3">
                <div className={`flex h-14 w-14 shrink-0 items-center justify-center rounded-2xl text-xl font-bold shadow-sm ${getInvestmentGradeTone(scoreSummary.grade)}`}>
                  {scoreSummary.grade || '—'}
                </div>
                <div className="min-w-0">
                  <div className="flex items-center gap-1.5 text-xs font-medium uppercase tracking-[0.14em] text-muted-foreground">
                    <Sparkles className="h-3 w-3 text-brand-500" />
                    {scoreType}
                  </div>
                  <p className="mt-1 line-clamp-1 text-sm font-semibold text-foreground">{recommendation}</p>
                  {scoreSummary.partialLabel && (
                    <p className="mt-0.5 line-clamp-1 text-[11px] text-muted-foreground">{scoreSummary.partialLabel}</p>
                  )}
                </div>
              </div>
              <div className="shrink-0 text-right">
                {scoreSummary.score == null ? (
                  <>
                    <div className="text-sm font-semibold text-muted-foreground">Insufficient data</div>
                    <div className="text-xs text-muted-foreground">score pending</div>
                  </>
                ) : (
                  <>
                    <div className={`text-3xl font-bold tracking-tight ${getScoreTone(scoreSummary.score)}`}>{scoreSummary.score}</div>
                    <div className="text-xs text-muted-foreground">/100 score</div>
                  </>
                )}
              </div>
            </div>
          </div>
        ) : hasAreaPlaceholder ? (
          <div className="rounded-2xl border border-dashed border-border/70 bg-muted/25 p-3">
            <div className="flex items-center gap-3">
              <div className="flex h-12 w-12 items-center justify-center rounded-2xl bg-muted text-muted-foreground">
                <Map className="h-5 w-5" />
              </div>
              <div>
                <div className="text-sm font-semibold text-foreground">Area Score Pending</div>
                <p className="text-xs text-muted-foreground">Generate a new report to calculate area score</p>
              </div>
            </div>
          </div>
        ) : null}
      </CardContent>

      <CardFooter className="relative flex flex-col gap-2 border-t border-border/60 bg-muted/20 p-4">
        <div className="flex w-full gap-2">
          <Button variant="default" size="sm" onClick={() => onView(report)} className="flex-1 gap-1.5 rounded-xl">
            <Eye className="h-3.5 w-3.5" />
            View
          </Button>
          <Button variant="outline" size="sm" onClick={() => onDownload(report)} className="flex-1 gap-1.5 rounded-xl">
            <Download className="h-3.5 w-3.5" />
            Download
          </Button>
        </div>
        <RegenerateReportButton
          reportId={report.id}
          propertyAddress={report.property_address}
          onRegenerated={onRegenerated}
          variant="ghost"
          size="sm"
          className="w-full justify-center rounded-xl"
        />
        {/* PDF download is available inside the report viewer (View) to avoid loading huge report payloads in the list. */}
      </CardFooter>
    </Card>
  );
}
