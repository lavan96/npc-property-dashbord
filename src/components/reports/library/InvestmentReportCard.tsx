import { format } from 'date-fns';
import { Calendar, Download, Eye, Home, Building2, Map, Globe, MapPin, TrendingUp, User, Zap } from 'lucide-react';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Checkbox } from '@/components/ui/checkbox';
import { RegenerateReportButton } from '@/components/reports/RegenerateReportButton';
import { ReportActionMenu, type ReportActionStatus } from '@/components/reports/ReportActionMenu';
import { TierBadge, type ReportTier } from '@/components/reports/TierBadge';
import type { InvestmentReport } from './types';

interface InvestmentReportCardProps {
  report: InvestmentReport;
  isSelected: boolean;
  canAddMore: boolean;
  isAutoGenerated: boolean;
  generatingTier: ReportTier | null;
  canEditReports: boolean;
  generatorLabel: (id?: string | null) => string;
  getGradeColor: (grade: string) => string;
  getScoreColor: (score: number) => string;
  onToggleSelection: (report: InvestmentReport, checked: boolean) => void;
  onView: (report: InvestmentReport) => void;
  onDownload: (report: InvestmentReport) => void;
  onRegenerated: () => void;
  onViewHistory: (report: InvestmentReport) => void;
  onToggleArchive: (report: InvestmentReport) => void;
  onGenerateTier: (report: InvestmentReport, targetTier: ReportTier) => void;
}

export function InvestmentReportCard({ report, isSelected, canAddMore, isAutoGenerated, generatingTier, canEditReports, generatorLabel, getGradeColor, getScoreColor, onToggleSelection, onView, onDownload, onRegenerated, onViewHistory, onToggleArchive, onGenerateTier }: InvestmentReportCardProps) {
  return (
    <Card className="overflow-hidden hover:shadow-lg transition-shadow relative">
      <div className="absolute top-4 right-4 z-10">
        <Checkbox checked={isSelected} onCheckedChange={(checked) => onToggleSelection(report, checked as boolean)} disabled={!canAddMore && !isSelected} className="h-5 w-5 bg-background border-2" />
      </div>
      <CardHeader>
        <CardTitle className="text-lg flex items-start justify-between pr-8">
          <div className="flex flex-col gap-1">
            <span className="line-clamp-2">{report.property_address}</span>
            {report.report_scope && (
              <Badge variant="secondary" className={`text-xs w-fit flex items-center gap-1 ${report.report_scope === 'address' ? 'bg-blue-100 text-blue-700 hover:bg-blue-200 dark:bg-blue-900/30 dark:text-blue-300' : report.report_scope === 'suburb' ? 'bg-green-100 text-green-700 hover:bg-green-200 dark:bg-green-900/30 dark:text-green-300' : report.report_scope === 'zipcode' ? 'bg-yellow-100 text-yellow-700 hover:bg-yellow-200 dark:bg-yellow-900/30 dark:text-yellow-300' : 'bg-purple-100 text-purple-700 hover:bg-purple-200 dark:bg-purple-900/30 dark:text-purple-300'}`}>
                {report.report_scope === 'address' && <><Home className="h-3 w-3" />Property Analysis</>}
                {report.report_scope === 'suburb' && <><Building2 className="h-3 w-3" />Suburb Analysis</>}
                {report.report_scope === 'zipcode' && <><Map className="h-3 w-3" />Postcode Analysis</>}
                {report.report_scope === 'state' && <><Globe className="h-3 w-3" />State Analysis</>}
              </Badge>
            )}
          </div>
          <div className="flex items-center gap-2">
            {report.report_tier && <TierBadge tier={report.report_tier} showIcon={false} />}
            {report.report_variant && report.report_variant !== 'composite' && <Badge variant="outline" className="text-xs">{report.report_variant === 'financial' ? 'FIN' : 'PLDD'}</Badge>}
            {isAutoGenerated && <Badge variant="secondary" className="text-xs bg-violet-100 text-violet-700 hover:bg-violet-200 dark:bg-violet-900/30 dark:text-violet-300"><Zap className="h-3 w-3 mr-1" />Auto</Badge>}
            {report.status === 'pending' && <Badge variant="outline" className="text-xs">Pending</Badge>}
            <TrendingUp className="h-5 w-5 text-muted-foreground flex-shrink-0" />
          </div>
        </CardTitle>
        <CardDescription className="text-xs flex items-center gap-1"><Calendar className="h-3 w-3" />Generated on {format(new Date(report.created_at), 'PPp')}</CardDescription>
        <CardDescription className="text-xs flex items-center gap-1"><User className="h-3 w-3" />Generated by {generatorLabel(report.generated_by)}</CardDescription>
      </CardHeader>
      <CardContent className="space-y-4">
        {report.investment_score ? (
          <div className="flex items-center justify-between p-3 rounded-lg bg-muted/50 border">
            <div className="flex items-center gap-3"><div className={`w-10 h-10 rounded-lg font-bold text-lg flex items-center justify-center ${getGradeColor(report.investment_score.grade)}`}>{report.investment_score.grade || 'N/A'}</div><div className="flex flex-col"><span className="text-xs text-muted-foreground">{report.investment_score?.scoreType === 'area' ? 'Area Grade' : 'Investment Grade'}</span><span className="text-sm font-medium">{report.investment_score.recommendation?.split(' ').slice(0, 2).join(' ') || 'Not rated'}</span></div></div>
            <div className="flex flex-col items-end"><span className="text-xs text-muted-foreground">Score</span><span className={`text-xl font-bold ${getScoreColor(report.investment_score.totalScore || 0)}`}>{report.investment_score.totalScore || 0}<span className="text-sm font-normal text-muted-foreground">/100</span></span></div>
          </div>
        ) : ['suburb', 'zipcode', 'state'].includes(report.report_scope || '') ? (
          <div className="flex items-center justify-between p-3 rounded-lg bg-muted/30 border border-dashed"><div className="flex items-center gap-3"><div className="w-10 h-10 rounded-lg font-bold text-sm flex items-center justify-center bg-muted text-muted-foreground"><Map className="h-5 w-5" /></div><div className="flex flex-col"><span className="text-xs text-muted-foreground">Area Score</span><span className="text-sm text-muted-foreground">Generate a new report to calculate area score</span></div></div></div>
        ) : null}
        <div className="space-y-2"><div className="flex items-center gap-2 text-sm"><MapPin className="h-3 w-3 text-muted-foreground" /><span className="text-muted-foreground truncate">{report.property_address}</span></div></div>
        <div className="flex flex-col gap-2 pt-2">
          <div className="flex gap-2"><Button variant="outline" size="sm" onClick={() => onView(report)} className="flex-1"><Eye className="mr-1 h-3 w-3" />View</Button><Button variant="default" size="sm" onClick={() => onDownload(report)} className="flex-1"><Download className="mr-1 h-3 w-3" />Download</Button></div>
          <div className="flex gap-2"><RegenerateReportButton reportId={report.id} propertyAddress={report.property_address} onRegenerated={onRegenerated} variant="outline" size="sm" className="flex-1" /><ReportActionMenu surface="report-card" label={report.property_address} report={{ reportId: report.id, status: (report.status as ReportActionStatus) || 'completed', tier: report.report_tier, scope: report.report_scope, isArchived: !!report.is_archived }} generatingTier={generatingTier as 'briefing' | 'snapshot' | null} callbacks={{ onViewHistory: () => onViewHistory(report), onToggleArchive: () => onToggleArchive(report), onGenerateBriefing: report.report_tier === 'compass' ? () => onGenerateTier(report, 'briefing') : undefined, onGenerateSnapshot: report.report_tier === 'compass' ? () => onGenerateTier(report, 'snapshot') : undefined }} permissions={{ canArchive: canEditReports }} triggerClassName="h-8 w-8" /></div>
        </div>
      </CardContent>
    </Card>
  );
}
