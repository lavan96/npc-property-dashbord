import { format } from 'date-fns';
import { Download, Eye } from 'lucide-react';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Checkbox } from '@/components/ui/checkbox';
import { RegenerateReportButton } from '@/components/reports/RegenerateReportButton';
import { ReportActionMenu, type ReportActionStatus } from '@/components/reports/ReportActionMenu';
import { TierBadge, type ReportTier } from '@/components/reports/TierBadge';
import type { InvestmentReport } from './types';
import { getReportVariantLabel } from '@/lib/reports/reportVariants';

interface InvestmentReportTableProps {
  reports: InvestmentReport[];
  isSelected: (reportId: string) => boolean;
  canAddMore: boolean;
  isAutoGenerated: (reportId: string) => boolean;
  generatingTier: { reportId: string; tier: ReportTier } | null;
  canEditReports: boolean;
  generatorLabel: (id?: string | null) => string;
  getGradeColor: (grade: string) => string;
  getScoreColor: (score: number) => string;
  onToggleSelection: (report: InvestmentReport, checked: boolean) => void;
  onView: (report: InvestmentReport) => void;
  onDownload: (report: InvestmentReport) => void;
  onRegenerated: () => void;
  onViewHistory: (report: InvestmentReport) => void;
  onToggleArchive: (report: InvestmentReport) => void;
  onGenerateTier: (report: InvestmentReport, targetTier: ReportTier) => void;
}

const scopeLabels: Record<string, string> = {
  address: 'Property',
  suburb: 'Suburb',
  zipcode: 'Postcode',
  state: 'State',
};

export function InvestmentReportTable({
  reports,
  isSelected,
  canAddMore,
  isAutoGenerated,
  generatingTier,
  canEditReports,
  generatorLabel,
  getGradeColor,
  getScoreColor,
  onToggleSelection,
  onView,
  onDownload,
  onRegenerated,
  onViewHistory,
  onToggleArchive,
  onGenerateTier,
}: InvestmentReportTableProps) {
  return (
    <div className="overflow-x-auto rounded-2xl border border-border/70 bg-card/90 shadow-sm dark:bg-background/70">
      <table className="w-full min-w-[1180px] text-sm">
        <thead className="border-b border-border/70 bg-muted/40 text-xs uppercase tracking-[0.14em] text-muted-foreground">
          <tr>
            <th className="w-12 px-4 py-3 text-left">Select</th>
            <th className="px-4 py-3 text-left">Property / Area</th>
            <th className="px-4 py-3 text-left">Scope</th>
            <th className="px-4 py-3 text-left">Tier</th>
            <th className="px-4 py-3 text-left">Grade</th>
            <th className="px-4 py-3 text-left">Score</th>
            <th className="px-4 py-3 text-left">Source</th>
            <th className="px-4 py-3 text-left">Status</th>
            <th className="px-4 py-3 text-left">Generated</th>
            <th className="px-4 py-3 text-left">Generated By</th>
            <th className="px-4 py-3 text-right">Actions</th>
          </tr>
        </thead>
        <tbody className="divide-y divide-border/60">
          {reports.map((report) => {
            const selected = isSelected(report.id);
            const grade = report.investment_score?.grade || 'N/A';
            const score = report.investment_score?.totalScore || 0;
            const autoGenerated = isAutoGenerated(report.id);
            return (
              <tr key={report.id} className={selected ? 'bg-brand-500/5' : 'transition-colors hover:bg-muted/30'}>
                <td className="px-4 py-3 align-middle">
                  <Checkbox
                    checked={selected}
                    onCheckedChange={(checked) => onToggleSelection(report, checked as boolean)}
                    disabled={!canAddMore && !selected}
                    className="h-5 w-5 border-2 bg-background"
                    aria-label={`Select ${report.property_address} for comparison`}
                  />
                </td>
                <td className="max-w-[280px] px-4 py-3 align-middle">
                  <div className="line-clamp-2 font-semibold text-foreground">{report.property_address}</div>
                  <div className="mt-1 text-xs text-muted-foreground">Version {report.current_version || 1}</div>
                </td>
                <td className="px-4 py-3 align-middle">
                  <Badge variant="secondary" className="whitespace-nowrap text-xs">
                    {scopeLabels[report.report_scope || ''] || 'Report'}
                  </Badge>
                </td>
                <td className="px-4 py-3 align-middle">
                  {report.report_tier ? <TierBadge tier={report.report_tier} showIcon={false} /> : <span className="text-muted-foreground">—</span>}
                  {report.report_variant && report.report_variant !== 'composite' && (
                    <Badge variant="outline" className="ml-2 text-xs">
                      {getReportVariantLabel(report.report_variant)}
                    </Badge>
                  )}
                </td>
                <td className="px-4 py-3 align-middle">
                  {report.investment_score ? (
                    <span className={`inline-flex h-8 min-w-8 items-center justify-center rounded-lg px-2 text-sm font-bold ${getGradeColor(grade)}`}>{grade}</span>
                  ) : (
                    <span className="text-muted-foreground">—</span>
                  )}
                </td>
                <td className="px-4 py-3 align-middle">
                  {report.investment_score ? (
                    <span className={`font-semibold ${getScoreColor(score)}`}>{score}<span className="text-xs font-normal text-muted-foreground">/100</span></span>
                  ) : (
                    <span className="text-muted-foreground">—</span>
                  )}
                </td>
                <td className="px-4 py-3 align-middle">
                  <Badge variant={autoGenerated ? 'secondary' : 'outline'} className="whitespace-nowrap text-xs">
                    {autoGenerated ? 'Auto-generated' : 'Manual'}
                  </Badge>
                </td>
                <td className="px-4 py-3 align-middle">
                  <Badge variant={report.status && report.status !== 'completed' ? 'outline' : 'secondary'} className="whitespace-nowrap text-xs capitalize">
                    {report.status || 'completed'}
                  </Badge>
                </td>
                <td className="whitespace-nowrap px-4 py-3 align-middle text-muted-foreground">
                  {format(new Date(report.created_at), 'MMM dd, yyyy')}
                </td>
                <td className="max-w-[180px] truncate px-4 py-3 align-middle text-muted-foreground">
                  {generatorLabel(report.generated_by)}
                </td>
                <td className="px-4 py-3 align-middle">
                  <div className="flex justify-end gap-1.5">
                    <Button variant="ghost" size="icon" onClick={() => onView(report)} title="View report" className="h-8 w-8">
                      <Eye className="h-4 w-4" />
                    </Button>
                    <Button variant="ghost" size="icon" onClick={() => onDownload(report)} title="Download report" className="h-8 w-8">
                      <Download className="h-4 w-4" />
                    </Button>
                    <RegenerateReportButton
                      reportId={report.id}
                      propertyAddress={report.property_address}
                      onRegenerated={onRegenerated}
                      variant="ghost"
                      size="sm"
                      className="h-8 rounded-lg px-2 text-xs"
                    />
                    <ReportActionMenu
                      surface="report-card"
                      label={report.property_address}
                      report={{
                        reportId: report.id,
                        status: (report.status as ReportActionStatus) || 'completed',
                        tier: report.report_tier,
                        scope: report.report_scope,
                        isArchived: !!report.is_archived,
                      }}
                      generatingTier={generatingTier?.reportId === report.id ? generatingTier.tier as 'briefing' | 'snapshot' : null}
                      callbacks={{
                        onViewHistory: () => onViewHistory(report),
                        onToggleArchive: () => onToggleArchive(report),
                        onGenerateBriefing: report.report_tier === 'compass' ? () => onGenerateTier(report, 'briefing') : undefined,
                        onGenerateSnapshot: report.report_tier === 'compass' ? () => onGenerateTier(report, 'snapshot') : undefined,
                      }}
                      permissions={{ canArchive: canEditReports }}
                      triggerClassName="h-8 w-8 rounded-full"
                    />
                  </div>
                </td>
              </tr>
            );
          })}
        </tbody>
      </table>
    </div>
  );
}
